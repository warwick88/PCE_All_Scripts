/*
	So we have some ServiceID's to review. They are here! using SA_RCDID as the serviceID.
	When you go to SAL master list you will also find these services. First few show reported.
*/
SELECT P1.*,
CASE
	WHEN SA_ENCSTS = 'Y' THEN 'Sent'
	WHEN SA_ENCSTS = 'U' THEN 'Unreportable'
	WHEN SA_ENCSTS = 'N' THEN 'Not Sent'
	else 'nothing'
END AS 'Reporting Status'
FROM PCHSALPF P1
WHERE P1.SA_RCDID in (
44943
